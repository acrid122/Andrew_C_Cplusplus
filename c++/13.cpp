class Shape
{
public:
    Shape(int x, int y): x{x}, y{y}
    {}
    virtual double getSquare() const = 0; //площадь фигуры
    virtual double getPerimeter() const = 0; //периметр фигуры
    void printCoords() const
    {
        cout << "X: " << x << "\tY: " << y << endl;
    }
private:
    int x;
    int y;
};

class Rectangle : public Shape //класс прямоугольника
{
public:
    Rectangle(int x, int y, double w, double h) : Shape{x, y}, width(w), height(h) {}
    double getSquare() const override
    {
        return width * height;
    }
    double getPerimeter() const override
    {
        return width * 2 + height * 2;
    }
private:
    double width; //ширина
    double height; //высота
};

class Circle : public Shape // круг
{
public:
    Circle (int x, int y, double r) : Shape{x, y}, radius(r) {}
    double getSquare() const override
    {
        return radius * radius * 3.14;
    }
    double getPerimeter() const override
    {
        return 2 * 3.14 * radius;
    }
private:
    double radius; //радиус круга
};


class Counter
{
public:
    Counter(int val)
    {
        value = val;
    }
    void print()
    {
        cout << "Counter value: " << value << endl;
    }
    Counter operator + (const Counter& counter) const
    {
        return Counter(value + counter.value);
    }

    bool operator == (const Counter& counter) const
    {
        return value == counter.value;
    }
    bool operator != (const Counter& counter) const
    {
        return value != counter.value;
    }
    bool operator > (const Counter& counter) const
    {
        return value > counter.value;
    }
    bool operator < (const Counter& counter) const
    {
        return value < counter.value;
    }
    //оператор присвоения. оператор присвоения обычно возвращает ссылку на свой левый операнд
    Counter& operator += (const Counter& counter)
    {
        value += counter.value;
        return *this; //возвращаем ссылку на текущий объект
    }
    //оператор унарного минуса
    Counter operator - () const
    {
        return Counter{-value}; //возвращает новый объект типа Counter, значение которого равно отрицательному значению текущего объекта
    }
    //операции инкремента и декремента
    //префиксные операторы
    Counter& operator ++ ()
    {
        value += 1;
        return *this; //возвращаем ссылку на текущий объект
    }
    Counter& operator -- ()
    {
        value -= 1;
        return *this; //возвращаем ссылку на текущий объект
    }
    //постфиксные операторы
    Counter operator ++ (int)
    {
        Counter temp = *this; //сохраняем текущее значение объекта
        value += 1; //увеличиваем значение на 1
        return temp; //возвращаем сохраненное значение
    }
    Counter operator -- (int)
    {
        Counter temp = *this; //сохраняем текущее значение объекта
        value -= 1; //уменьшаем значение на 1
        return temp; //возвращаем сохраненное значение
    }
    //Постфиксная форма возвращает копию объекта до инкремента или декремента, а префиксная форма возвращает ссылку на объект после изменения его значения.
    //Чтобы постфиксная форма отличалась от префиксной, в ее объявлении используется дополнительный параметр типа int, который не используется в теле функции.
    int getValue() const {return value;}
private:
    int value;
};

    //Переопределение оператора << для вывода объекта класса Counter в поток вывода
    /*
    оператор << принимает два аргумента: ссылку на объект потока (левый операнд) и фактическое значение для вывода (правый операнд).
    Затем он возвращает новую ссылку на поток, которую можно передать при следуем вызове оператора << в цепочке вызовов. 
    Это позволяет использовать оператор << для вывода нескольких значений в одной строке, например: cout << "Counter value: " << counter << endl;
    */
ostream operator << (ostream& stream, const Counter& counter)
{
    stream << "Value: ";
    stream << counter.getValue();
    return stream; //возвращаем ссылку на поток
}



