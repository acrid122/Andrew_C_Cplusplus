class Person
{
public:
    Person(string name): name{name} {}
    virtual ~Person()
    {
        cout << "Person " << name << " deleted" << endl;
    }
    virtual void print() const{
        cout << "Name: " << name << endl;
    }
    string getName() const {return name;}
private:
    string name;
};

class Employee: public Person
{
public:
    Employee(string name, string company): Person{name}, company{company} {}
    void print() const override //функция print переопределена в классе Employee
    {
        Person::print();
        cout << getName() << " (" << company << ")" << endl;
    }
    ~Employee()
    {
        cout << "Employee " << getName() << " deleted" << endl;
    }
private:
    string company;
};

void printPerson(const Person& person) //параметр функции - ссылка на объект базового класса
{
    person.print(); //вызов функции print для объекта person
}
