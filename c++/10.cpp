void calculate(int n1, int n2, Operation op)
{
    switch (op)
    {
        case Operation::Add:
            cout << "Result: " << n1 + n2 << endl;
            break;
        case Operation::Subtract:
            cout << "Result: " << n1 - n2 << endl;
            break;
        case Operation::Multiply:
            cout << "Result: " << n1 * n2 << endl;
            break;
        case Operation::Divide:
            cout << "Result: " << n1 / n2 << endl;
            break;  
    }
}
