#include <iostream>
#include <string>

using namespace std;
class Person
{
public:
    string name;
    unsigned age;
    mutable string cached_info; //пример поля, которое может быть изменяемым, даже если объект является константным
    Person(string p_name, unsigned p_age, string p_cached_info)
    {
        name = p_name;
        age = p_age;
        cached_info = p_cached_info;
    }
    const string& getName() const //геттер для получения значения поля name, возвращающий константную ссылку на строку, так как мы не хотим изменять строку, и не хотим копировать ее при возврате
    {
        return name;
    }
    const unsigned* getAge() const //геттер для получения значения поля age, возвращающий константный указатель на unsigned, так как мы не хотим изменять значение age, и не хотим копировать его при возврате
    {
        return &age;
    }
    void print() const //константная функция-член, которая не изменяет состояние объекта, может быть вызвана для константного объекта
    {
        cout << "Name: " <<  name << "\tAge: " << age << endl;
        cout << "Cached Info: " << cached_info << endl;
        //если попытаться внутри функции вызвать неконстантные другие функции, то будет ошибка.
    }
    //Еще одно ограничение, связанное с константными функциями состоит в том, что если мы хотим возвратить из константной функции 
    //указатель или ссылку, то указатель должен указывать на константу, а ссылка должны быть константной.
};


class Point
{
public:
    Point(int x, int y)
    {
        this->x = x; //используем указатель this, чтобы обратиться к полю x текущего объекта, и присвоить ему значение параметра x
        this->y = y; //используем указатель this, чтобы обратиться к полю y текущего объекта, и присвоить ему значение параметра y  
    }
    void showCoords()
    {
        cout << "X: " << this->x << "\tY: " << this->y << endl;
    }
private:
    int x;
    int y;
};

class Point1
{
public:
    Point1(int x, int y)
    {
        this->x = x;
        this->y = y;
    }
    void showCoords()
    {
        cout << "X: " << this->x << "\tY: " << this->y << endl;
    }  
    Point1 &move(int x, int y)
    {
        this->x += x; //двигаем точку на x единиц по горизонтали
        this->y += y; //двигаем точку на y единиц по вертикали
        return *this; //возвращаем текущий объект класса Point, разыменовывая указатель this
    }
private:
    int x;
    int y;
};


