    /*
    Зачем нужны геттеры и сеттеры, если они просто копируют доступ к полям класса? 
    Они позволяют контролировать доступ к полям класса. Например, мы можем запретить устанавливать отрицательный возраст,
    или мы можем запретить устанавливать слишком длинное имя. Геттеры и сеттеры позволяют нам контролировать, какие значения могут быть установлены в поля класса, и таким образом обеспечивают инкапсуляцию и защиту данных.
    */

    /*
    Что позволяет делать сеттер:
    Сеттер может:
    проверять диапазон значений, чтобы гарантировать, что поле класса всегда содержит допустимые данные;
    отбрасывать недопустимые значения, предотвращая нарушение логики программы;
    выбрасывать исключения при попытке установить недопустимое значение, что позволяет обрабатывать ошибки на более высоком уровне;
    приводить данные к нужному формату, например, обрезать строку до определенной длины или преобразовывать текст в верхний регистр.
    вызывать пересчет или обновление других связанных полей, если изменение одного поля влияет на другие.
    вести журнал изменений, например, записывать в лог каждое изменение значения поля для последующего анализа.
    */
   Person7 tom7{"Tom7", 48};
