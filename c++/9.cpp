   /*
   Спецификатор private. При помощи данного спецификатора можно скрыть реализацию членов класса, то есть сделать их закрытми, инкапсулировать внутри класса.
   Инкапсуляция - один из принципов ООП, заключающийся в объединении данных и методов, которые работают с этими данными, внутри одного класса и скрытии внутренность устройство
   объекта от внешнего взаимодействия. Создает "черный ящик", ограничивая прямой доступ к компонентами (приватность) и обеспечивая взаимодействие только через геттеры и сеттеры (интерфейс).
   */

